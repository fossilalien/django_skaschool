$(document).ready(function() {
    // add class "skaschool-home" to "body"
    if (! $("body").hasClass("skaschool-home")) {
        $("body").addClass("skaschool-home");
    }
    // add class "masthead" to "main"
    if (! $("main").hasClass("masthead")) {
        $("main").addClass("masthead");
    }
});

